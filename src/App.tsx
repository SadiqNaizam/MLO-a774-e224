import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";


import CodeDisplayView from "./pages/CodeDisplayView";
import LoadingStatusView from "./pages/LoadingStatusView";
import UIDesignPreviewView from "./pages/UIDesignPreviewView";
import NotFound from "./pages/NotFound";

const queryClient = new QueryClient();


const App = () => (
<QueryClientProvider client={queryClient}>
    <TooltipProvider>
    <Toaster />
    <Sonner />
    <BrowserRouter>
        <Routes>


          <Route path="/" element={<CodeDisplayView />} />
          <Route path="/loading-status" element={<LoadingStatusView />} />
          <Route path="/u-i-design-preview" element={<UIDesignPreviewView />} />
          {/* catch-all */}
          <Route path="*" element={<NotFound />} />


        </Routes>
    </BrowserRouter>
    </TooltipProvider>
</QueryClientProvider>
);

export default App;
